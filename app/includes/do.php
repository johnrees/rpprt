              <h2>
                The Solution
              </h2>
              <p>
                We developed an event identity which provided a strong theme that the employees could identify with and would continue to be motivated by post event. An experiential tunnel saw the employees using UV torches to discover historic advertising and poignant quotes, while listening to nostalgic adverts and jingles, this took them back to their roots and reminded them of past success. In the main room Projection Mapping content across a huge 3D stage fully immersed the audience in the plan, encouraging real interactivity through Q&amp;A and workshop sessions.
              </p>
