        Rapport and Kellogg's; the start of a beautiful friendship...
      </h3>
      <h5>
        By Elena Clarke, 15th December 2011
      </h5>
      <div class="post-wrapper table">
        <div class="body cell table">
          <div class="media cell">
            <img alt="Rapport_and_kelloggs" src="/blog/rapport_and_kelloggs.jpg?1324028900">
          </div>
          <div class="col cell">
            <p>
              As if December wasn't already exciting enough, last week we delivered the annual UK Conference for a new Rapport client, Kellogg's.
            </p>
            <p>
              Given the time of year the 'Journey to the North Pole' theme was pretty fitting. We created 3 spaces, Basecamp Cornelius, the nostalgic Kellogg's 'Heritage Tunnel' and the main Conference space, into which we put an enormous 3D Iceberg stage set, we projected images and video content across the entire structure for maximum presentation engagement.
            </p>
          </div>
          <div class="col cell">
            <p>
              A workshop in the afternoon contained a talk from 9 times Polar explorer Alan Chambers (who we highly recommend!) and a bespoke interactive activity which was designed to get delegates into the winning mindset, ready for 2012, it was all to play for with a money can't buy prize!
            </p>
            <p>
              To finish things off with a bang the room was transformed into the 'Ice Bar' for the evening party, the Ice Bar boasted an illuminated cocktail bar and furniture and entertainment came in the form of a live band and even Bandeoki for those Kellogg's employees who were brave enough to get up and sing along with the band.
            </p>
            <p>
              Fingers crossed we hope we can help Kellogg's Do many more Great Things in 2012!
