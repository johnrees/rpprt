          </div>
          <div class="col cell">
            <p>
              Here at Rapport we like to change delegates into 'Doers', that means that the messaging and outcomes from a Conference do not stop when the event does; we judge a successful event by the amount of 'doing' that goes on afterwards, and it doesn't get much bigger on the Do Great Things scale than this one.
            </p>
            <p>
              Back in December of last year we delivered an event for Kellogg's with the theme 'Be Ready, Be Bold, Be Kellogg's' (<a onclick="$('li#do.primary a.heading').trigger('click'); return false;" href="http://goo.gl/lZZ1M">have a look to jog your memory</a>), the messaging, particularly 'Be Bold' has stuck and become somewhat of an internal comms campaign within the Kellogg's HQ. We are pretty chuffed about that, but we like to take things one step further, so as part of the Workshop session at the Conference we awarded the winning team a 'Be Bold' prize...
            </p>
          </div>
          <div class="col cell">
            <p>
              As it happens the guest speaker and facilitator for the bespoke Workshop we created was friend of Rapport and 9 times North Pole explorer, Alan Chambers, and whats more Alan was planning a trip to the North Pole for this April... so we've put the winning group's names on a flag and good old Alan is going to carry it with him all the way to the top of the world and take a shot for us all to see - now thats being Bold!
            </p>
            <p>
              Heres the flag, for now, against the much less scenic backdrop of the office wall...
