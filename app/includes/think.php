      <h3>
        Climb Snowdon, just check the weather first.
      </h3>
      <h5>
        By Elena Clarke, 9th June 2011
      </h5>
      <div class="post-wrapper table">
        <div class="body cell table">
          <div class="media cell">
            <img alt="Snowdon" src="/blog/snowdon.jpg?1308668180">
          </div>
          <div class="col cell">
            <p>
              Like most good ideas it all started in the pub, one Friday night back in March Adam said: "I was thinking for my 30th I might climb Snowdon, what do you think?" Me: "Amazing idea Adam, I'd definitely be up for that". Two months later...
            </p>
            <p>
              You'd forgive us for thinking that in May the weather at the top of a mountain might be 'fair', some of us even sported jeans for the climb, but no the rain was torrential, the winds were gale-force and the temperature was sub zero. Despite it all spirits were high and we kept in mind that we were ticking an experience off the list, celebrating a birthday doing something pro-active, rather than doing an all-dayer and a wood-burning stove and more to the point, a lot of alcohol was waiting for us in the house we'd hired back on sea level.
            </p>
          </div>
          <div class="col cell">
            <p>
              The highlight of the trip was having the whole of the cafe at the top of Snowdon singing happy birthday to Adam, the low point quickly followed this when we found out the train we'd planned to get back down from the cafe had stopped running due to 'adverse weather conditions'.
            </p>
            <p>
              Anyway, we made it down alive and learnt something that day that they don't tell you at the till in Sports Direct; the term 'water-proof' only extends to a certain point (like a rain-shower in Salford) and does not necessarily apply when up a mountain.
            </p>
            <p>
              and no trip is complete without trying to make it trend on Twitter, so if you want to join our discussion its #WET.
