      <h3>
        A Timeline of Your Life
      </h3>
      <h5>
        By Paul Rose, 23rd September 2011
      </h5>
      <div class="post-wrapper table">
        <div class="body cell table">
          <div class="media cell">
            <img alt="Feltron" src="/blog/feltron.jpg?1316798575">
          </div>
          <div class="col cell">
            <p>
              Here at Rapport we love timelines… check out the recent installation we produced for Adidas. So it comes as something of a delight to learn that one of Facebook's shiny new features - announced yesterday by Mark Zuckerberg at F8 - is just that, a timeline of your life.
            </p>
            <p>
              The features of the timeline, such as its integration of personal info, photos and what movies and music you've been listening to, will all be explained on more appropriate techie blogs than this but what many might not be aware is where much of its inspiration came from. Particularly its aesthetic and design.
            </p>
            <p>
              Enter Nicholas Feltron, a graphic designer based in NYC, Back in 2005 Feltron began documented his everyday life in the minutest detail.
            </p>
          </div>
          <div class="col cell">
            <p>
              Covering everything from the taxi fares he paid to his most listened to album, Feltron amassed so much data he produced an annual report which documented his entire life in that year.
            </p>
            <p>
              Now, apart from the incredible commitment and attention to detail shown by Feltron to produce such work, what really stands out to us is the beautifully crafted info-graphics which communicate his results. Check out samples from each of his reports (there are six in total, dating from 2005 - 2010) on his website. The work on show was enough to land him a role on the Facebook product design team… a true example of doing great things.<br>
